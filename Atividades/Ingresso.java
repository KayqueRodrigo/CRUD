public class Ingresso {
    private double valorIngeresso;

    public Ingresso() {
        this.valorIngeresso = valorIngeresso;
    }

    public double getValorIngeresso() {
        return valorIngeresso;
    }

    public void setValorIngeresso(double valorIngeresso) {
        this.valorIngeresso = valorIngeresso;
    }

    public double imprimeValor(){
        return getValorIngeresso();
    }
}
